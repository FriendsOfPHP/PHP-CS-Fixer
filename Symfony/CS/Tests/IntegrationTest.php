namespace Symfony\CS\Tests;

/**
 * Test that parses and runs the fixture '*.test' files found in '/Fixtures/Integration'.
 *
 * @author SpacePossum <possumfromspace@gmail.com>
 *
 * @internal
 */
final class IntegrationTest extends AbstractIntegrationTest
{
    /**
     * {@inheritdoc}
     */
    protected static function getFixturesDir()
    {
        return __DIR__.'/Fixtures/Integration';
    }

    /**
     * {@inheritdoc}
     */
    protected static function getTempFile()
    {
        return self::getFixturesDir().'/tmp/tmp.php';
    }

    /**
     * {@inheritdoc}
     */
    protected function doTestIntegration($testFileName, $testTitle, $fixers, $input, $expected = null)
    {
        if (defined('HHVM_VERSION') && in_array($testFileName, $this->getExcludeHHVMFileNames(), true)) {
            return;
        }

        parent::doTestIntegration($testFileName, $testTitle, $fixers, $input, $expected); // TODO: Change the autogenerated stub
    }

    /**
     * @return string[]
     */
    private function getExcludeHHVMFileNames()
    {
        return array(
            // short_echo_tag setting is ignored by HHVM
            // @see https://github.com/facebook/hhvm/issues/4809
            'echo_to_print,short_echo_tag.test',
        );
    }
}
